package Entity;

/**
 *
 * @author Admin
 */
public class StudentList {
    private String name;
    private String rollNumber;

    public StudentList() {
    }

    public StudentList(String name, String rollNumber) {
        this.name = name;
        this.rollNumber = rollNumber;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getRollNumber() {
        return rollNumber;
    }

    public void setRollNumber(String rollNumber) {
        this.rollNumber = rollNumber;
    }

    @Override
    public String toString() {
        return "StudentList{" + "name=" + name + ", rollNumber=" + rollNumber + '}';
    }
    
}
